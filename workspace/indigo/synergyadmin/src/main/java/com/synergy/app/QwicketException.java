package com.synergy.app;

public class QwicketException extends RuntimeException{
    public QwicketException() {
    }

    public QwicketException(final Throwable cause) {
        super(cause);
    }

    public QwicketException(final String message) {
        super(message);
    }

    public QwicketException(final String message, final Throwable cause) {
        super(message, cause);
    }
}
